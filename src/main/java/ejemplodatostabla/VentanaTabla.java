    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(null, "Por favor, seleccione un producto para modificar.");
        return;
    }

    String nombre = jTextField1.getText().trim();
    String precio = jTextField2.getText().trim();
    String categoria = jTextField3.getText().trim();
    String codigodeproducto = jTextField4.getText().trim();

    if (nombre.isEmpty() || precio.isEmpty() || categoria.isEmpty() || codigodeproducto.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.");
        return;
    }

    
    DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    modelo.setValueAt(nombre, selectedRow, 0);
    modelo.setValueAt(precio, selectedRow, 1);
    modelo.setValueAt(categoria, selectedRow, 2);
    modelo.setValueAt(codigodeproducto, selectedRow, 3);

    
    ActualizarArchivo();
                       
    }//GEN-LAST:event_jButton2ActionPerformed

