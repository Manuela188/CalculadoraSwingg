    public void CargarDatos(){
     BufferedReader bufferedReader = null;
        try {
            
            File file = new File("C:\\Users\\Usuario\\Documents\\NetBeansProjects\\CalculadoraSwingg\\EjemploDatosTabla\\src\\main\\java\\ejemplodatostabla\\archivo.txt");
             if (!file.exists()) {
            System.out.println("El archivo no existe: " + file.getAbsolutePath());
            return;
        }
        
        bufferedReader = new BufferedReader(new FileReader(file));
        
        String linea;
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0); 
        
       
        while ((linea = bufferedReader.readLine()) != null) {
            System.out.println("Leyendo linea: " + linea);
           
            String[] datosFila = linea.split(",");
            
            
            if (datosFila.length == 4) {
                modelo.addRow(datosFila); 
            } else {
                System.out.println("La linea no tiene el formato correcto: " + linea);
            }
        }
    } catch (Exception e) {
        System.out.println("Error al leer el archivo: " + e.getMessage());
    } finally {
        try {
            if (bufferedReader != null) {
                bufferedReader.close(); 
            }
        } catch (Exception ex) {
            System.out.println("Error al cerrar el lector: " + ex.getMessage());
        }
    }
}
    
     public void AgregarProducto(String nombre, String precio, String categoria, String codigodeproducto) {
        BufferedWriter bufferedWriter = null;
        try {
            File file = new File("C:\\Users\\Usuario\\Documents\\NetBeansProjects\\CalculadoraSwingg\\EjemploDatosTabla\\src\\main\\java\\ejemplodatostabla\\archivo.txt");
            bufferedWriter = new BufferedWriter(new FileWriter(file, true)); 
            bufferedWriter.write(nombre + "," + precio + "," + categoria + "," + codigodeproducto);
            bufferedWriter.newLine();
            
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.addRow(new String[]{nombre, precio, categoria, codigodeproducto});
            
           
            CargarDatos();
        } catch (Exception e) {
            System.out.println("Error al escribir en el archivo: " + e.getMessage());
        } finally {
            try {
                if (bufferedWriter != null) {
                    bufferedWriter.close();
                }
            } catch (Exception ex) {
                System.out.println("Error al cerrar el escritor: " + ex.getMessage());
            }
        }
    }
